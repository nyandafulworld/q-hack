(function() {
    // jQueryが読み込まれるまで待機
    function initSlider() {
        if (typeof jQuery === 'undefined') {
            console.log('jQuery not loaded yet, waiting...');
            setTimeout(initSlider, 100);
            return;
        }
        
        jQuery(document).ready(function($) {
            console.log('=== Q HACK Slider Debug ===');
            
            // Q HACK JAPAN 課題解決スライダー
            var $slider = $('.qhack-merit__slider');
            console.log('Slider element found:', $slider.length);
            
            if ($slider.length === 0) {
                console.log('Slider not found, exiting');
                return;
            }

            var $track = $slider.find('.qhack-merit__slider-track');
            var $slides = $slider.find('.qhack-merit__slide');
            // ページネーションはスライダーの外にあるため、親要素から検索
            var $meritSection = $slider.closest('.qhack-merit__inner');
            var $dots = $meritSection.find('.qhack-merit__pagination-dot');
            var $prevBtn = $meritSection.find('.qhack-merit__pagination-btn--prev');
            var $nextBtn = $meritSection.find('.qhack-merit__pagination-btn--next');
            
            console.log('Track found:', $track.length);
            console.log('Slides found:', $slides.length);
            console.log('Dots found:', $dots.length);
            console.log('Prev button found:', $prevBtn.length);
            console.log('Next button found:', $nextBtn.length);
            
            var currentIndex = 0;
            var totalSlides = $slides.length;
            var autoSlideInterval;
            var autoSlideDelay = 5000; // 5秒ごとに自動スライド

            function goToSlide(index) {
                if (index < 0) index = totalSlides - 1;
                if (index >= totalSlides) index = 0;
                
                currentIndex = index;
                var transformValue = 'translateX(-' + (currentIndex * 100) + '%)';
                $track.css('transform', transformValue);
                console.log('Going to slide:', currentIndex, 'Transform:', transformValue);
                
                // ドットのアクティブ状態を更新
                $dots.removeClass('active');
                $dots.filter('[data-index="' + currentIndex + '"]').addClass('active');
            }

            // 自動スライド開始
            function startAutoSlide() {
                autoSlideInterval = setInterval(function() {
                    goToSlide(currentIndex + 1);
                }, autoSlideDelay);
                console.log('Auto slide started');
            }

            // 自動スライド停止
            function stopAutoSlide() {
                clearInterval(autoSlideInterval);
            }

            // 自動スライドをリセット（手動操作後に再開）
            function resetAutoSlide() {
                stopAutoSlide();
                startAutoSlide();
            }

            // 前へボタン
            $prevBtn.on('click', function(e) {
                e.preventDefault();
                console.log('Prev button clicked');
                goToSlide(currentIndex - 1);
                resetAutoSlide();
            });

            // 次へボタン
            $nextBtn.on('click', function(e) {
                e.preventDefault();
                console.log('Next button clicked');
                goToSlide(currentIndex + 1);
                resetAutoSlide();
            });

            // ドットクリック
            $dots.on('click', function() {
                var index = parseInt($(this).data('index'));
                console.log('Dot clicked, index:', index);
                goToSlide(index);
                resetAutoSlide();
            });

            // マウスホバー時に自動スライドを一時停止
            $slider.on('mouseenter', function() {
                stopAutoSlide();
            });

            // マウスが離れたら自動スライドを再開
            $slider.on('mouseleave', function() {
                startAutoSlide();
            });

            // 初期表示は1枚目（index: 0）
            goToSlide(0);

            // 自動スライド開始
            startAutoSlide();
            
            console.log('=== Q HACK Slider initialized successfully ===');
            console.log('Total slides:', totalSlides);
        });
    }
    
    // 初期化開始
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', initSlider);
    } else {
        initSlider();
    }
})();
</script>

